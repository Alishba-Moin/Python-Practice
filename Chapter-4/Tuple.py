# TUPLE METHODS
# Tuples are immutable, meaning they cannot be changed after creation. Because of this, tuples have a limited set of methods

tuple1 = (2, 4, 6, 8, 10)

# Count Method
print(tuple1.count(4))

# Index Method
print(tuple1.index(8))

# Length Method
print(len(tuple1))

# Minimum Method
print(min(tuple1))

# Maximum Method
print(max(tuple1))

# Sum Method
print(sum(tuple1))
