# Integar
integar = 10
print("Integar", integar)
print(type(integar))  # Check type

# Float
float = 2.1
print("Float", float)
print(type(float))

# String
string = "Alishba Moin"
print("String", string)
print(type(string))

# Boolean
boolean = False
print("Boolean", boolean)
print(type(boolean))

# List
lists = [10, "list", 2.1]
print("List", lists)
print(type(lists))

# Tuple
tuple = (10, "Tuple", 2.1)
print("Tuple", tuple)
print(type(tuple))

# Set
set = {10, "set", 2.1}
print("Set", set)
print(type(set))

# Dictionary
dictionary = {"Name": "Alishba Moin", "Age": 19, "Country": "Pakistan"}
print("Dictionary", dictionary)
print(type(dictionary))

# None Type
none = None
print("None", none)
print(type(none))

# Bytes
bytes = b"Python"
print("Bytes", bytes)
print(type(bytes))

# Bytearray
byteArray = bytearray([2, 4, 6])
print("Byte Array", byteArray)
print(type(byteArray))

# Range
range = range(2)
print("Range", range)
print(type(range))

# Frozen Set
frozenSet = frozenset([11, 12, 13, 14])
print("Frozen Set", frozenSet)
print(type(frozenSet))

